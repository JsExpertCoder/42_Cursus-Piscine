#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (*(src + i) != '\0' && i < n)
	{
		*(dest + i) = *(src + i);
		i++;
	}
	while (i < n)
	{
		*(dest + i) = '\0';
		i++;
	}
	return (dest);
}

int	main()
{
	char	chaine[] = "Mouton";
	char	chaine2[10];
	unsigned int	size;

	size = 3;

	printf("%s \n\n", ft_strncpy(chaine2, chaine, size));
}
